def calcularAumentoDoValor(valor, taxaDeAumento):
    return valor + valor * taxaDeAumento / 100

def calcularReducaoDoValor(valor, taxaDeReducao):
    return valor - valor * taxaDeReducao / 100

def calcularDobroDoValor(valor):
    return valor * 2

def calcularMetadeDoValor(valor):
    return valor / 2