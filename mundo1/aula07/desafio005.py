# Aula 07 - Programa que recebe um número inteiro e mostra seu antecessor e sucessor

print('========== DESAFIO 005 ==========')
num = int(input('Digite um número: '))
antecessor = num - 1
sucessor = num + 1
print('Considerando o número {}, seu antecessor é {} e seu sucessor é {}'.format(num, antecessor, sucessor))